package it.openprj.jTicketing.blogic.model.entity;

import java.io.Serializable;
import java.math.BigDecimal;
import java.sql.Date;

public class PrezzoCategoriaTicket implements Serializable {

	private static final long serialVersionUID = 4341019338283839619L;
	private String uid;
	private String descrizione;
	private String condizioni;
	private BigDecimal prezzo;
	private Date dataInizio;
	private Date dataFine;

	public String getCondizioni() {
		return condizioni;
	}

	public void setCondizioni(String condizioni) {
		this.condizioni = condizioni;
	}

	public String getDescrizione() {
		return descrizione;
	}

	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}

	public Date getDataInizio() {
		return dataInizio;
	}

	public void setDataInizio(Date dataInizio) {
		this.dataInizio = dataInizio;
	}

	public Date getDataFine() {
		return dataFine;
	}

	public void setDataFine(Date dataFine) {
		this.dataFine = dataFine;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public BigDecimal getPrezzo() {
		return prezzo;
	}

	public void setPrezzo(BigDecimal prezzo) {
		this.prezzo = prezzo;
	}

}
